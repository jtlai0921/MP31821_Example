'''
   使用位元運算子
'''

x = 6; y = 13
#利用bin()函式將x, y轉為二進位
print(' 6 二進位 = ', bin(x)) 
print('13 二進位 = ', bin(y))

print('x & y = ', x & y)# And運算結果是0100再轉成十進位數值，得到4
print('x | y = ', x | y)# Or運算結果是1111再轉成十進位數值，得到15
print('x ^ y = ', x ^ y)# Xor運算，結果是1011再轉成十進位數值，得到11
print('~x = ', ~x)	# Not運算結果是1001，採2的補數，得-7，即-(x+1)
